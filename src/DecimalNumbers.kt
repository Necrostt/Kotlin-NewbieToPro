

fun main(){
    //Calculating the volume of a sphere using the formula: 0.75 * pi * radius^3
    val pi = 3.1415927F
    val radius = 5.5F
    val formulaValue = 0.75F
    val result = formulaValue * pi * radius * radius * radius

    println("The volume of the sphere with the radius $radius is: $result")


}